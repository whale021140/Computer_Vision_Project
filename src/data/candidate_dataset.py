import json
from pathlib import Path
from typing import Any, Dict, List, Optional

import torch
from PIL import Image
from torch.utils.data import Dataset


def _box_tensor(boxes: Any) -> torch.Tensor:
    """
    Convert a list of boxes to a float tensor with shape [N, 4].
    Empty box lists are converted to shape [0, 4].
    """
    if boxes is None or len(boxes) == 0:
        return torch.empty((0, 4), dtype=torch.float32)

    tensor = torch.tensor(boxes, dtype=torch.float32)

    if tensor.numel() == 0:
        return torch.empty((0, 4), dtype=torch.float32)

    return tensor.reshape(-1, 4)


class CandidateBoxDataset(Dataset):
    """
    PyTorch Dataset for candidate-set samples generated by
    src.data.build_candidate_samples.

    Each item corresponds to one image-expression pair and contains:
    - original image;
    - expression text;
    - candidate boxes;
    - candidate membership labels;
    - target boxes;
    - cardinality class;
    - metadata.

    The number of candidate boxes varies across samples, so this dataset
    should be used with candidate_collate_fn.
    """

    def __init__(
        self,
        candidate_file: str,
        image_root: str,
        load_image: bool = True,
        transform: Optional[Any] = None,
        max_samples: Optional[int] = None,
    ) -> None:
        self.candidate_file = Path(candidate_file)
        self.image_root = Path(image_root)
        self.load_image = load_image
        self.transform = transform

        if not self.candidate_file.exists():
            raise FileNotFoundError(f"Candidate file not found: {self.candidate_file}")

        if self.load_image and not self.image_root.exists():
            raise FileNotFoundError(f"Image root not found: {self.image_root}")

        self.samples: List[Dict[str, Any]] = []
        with self.candidate_file.open("r", encoding="utf-8") as f:
            for line_idx, line in enumerate(f):
                line = line.strip()
                if not line:
                    continue

                sample = json.loads(line)
                self.samples.append(sample)

                if max_samples is not None and len(self.samples) >= max_samples:
                    break

        if len(self.samples) == 0:
            raise ValueError(f"No samples loaded from {self.candidate_file}")

    def __len__(self) -> int:
        return len(self.samples)

    def _load_image(self, file_name: str) -> Image.Image:
        image_path = self.image_root / file_name

        if not image_path.exists():
            raise FileNotFoundError(f"Image file not found: {image_path}")

        image = Image.open(image_path).convert("RGB")
        return image

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        sample = self.samples[idx]

        image = None
        if self.load_image:
            image = self._load_image(sample["file_name"])
            if self.transform is not None:
                image = self.transform(image)

        candidate_boxes_xyxy = _box_tensor(sample.get("candidate_boxes_xyxy", []))
        candidate_boxes_norm = _box_tensor(sample.get("candidate_boxes_norm", []))
        target_boxes_xyxy = _box_tensor(sample.get("target_boxes_xyxy", []))
        target_boxes_norm = _box_tensor(sample.get("target_boxes_norm", []))

        candidate_labels = torch.tensor(
            sample.get("candidate_labels", []),
            dtype=torch.float32,
        )

        count_class = torch.tensor(sample["count_class"], dtype=torch.long)
        num_targets = int(sample.get("num_targets", target_boxes_xyxy.shape[0]))

        num_candidates = int(sample.get("num_candidates", candidate_boxes_xyxy.shape[0]))

        if candidate_boxes_xyxy.shape[0] != candidate_labels.shape[0]:
            raise ValueError(
                f"Candidate box / label length mismatch at idx={idx}: "
                f"{candidate_boxes_xyxy.shape[0]} boxes vs "
                f"{candidate_labels.shape[0]} labels"
            )

        if candidate_boxes_norm.shape[0] != candidate_boxes_xyxy.shape[0]:
            raise ValueError(
                f"Candidate normalized box length mismatch at idx={idx}: "
                f"{candidate_boxes_norm.shape[0]} normalized boxes vs "
                f"{candidate_boxes_xyxy.shape[0]} xyxy boxes"
            )

        metadata = {
            "sample_id": sample.get("sample_id"),
            "ref_id": sample.get("ref_id"),
            "sent_id": sample.get("sent_id"),
            "image_id": sample.get("image_id"),
            "file_name": sample.get("file_name"),
            "width": sample.get("width"),
            "height": sample.get("height"),
            "target_type": sample.get("target_type"),
            "num_targets": num_targets,
            "num_candidates": num_candidates,
            "target_ann_ids": sample.get("target_ann_ids", []),
            "candidate_ann_ids": sample.get("candidate_ann_ids", []),
        }

        return {
            "image": image,
            "expression": sample["expression"],
            "candidate_boxes_xyxy": candidate_boxes_xyxy,
            "candidate_boxes_norm": candidate_boxes_norm,
            "candidate_labels": candidate_labels,
            "count_class": count_class,
            "target_boxes_xyxy": target_boxes_xyxy,
            "target_boxes_norm": target_boxes_norm,
            "metadata": metadata,
        }


def candidate_collate_fn(batch: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Custom collate function for variable-size candidate sets.

    Candidate boxes and labels are kept as lists of tensors instead of being
    stacked, because each image-expression pair can have a different number
    of candidate boxes.
    """
    return {
        "images": [item["image"] for item in batch],
        "expressions": [item["expression"] for item in batch],
        "candidate_boxes_xyxy": [item["candidate_boxes_xyxy"] for item in batch],
        "candidate_boxes_norm": [item["candidate_boxes_norm"] for item in batch],
        "candidate_labels": [item["candidate_labels"] for item in batch],
        "count_class": torch.stack([item["count_class"] for item in batch], dim=0),
        "target_boxes_xyxy": [item["target_boxes_xyxy"] for item in batch],
        "target_boxes_norm": [item["target_boxes_norm"] for item in batch],
        "metadata": [item["metadata"] for item in batch],
    }